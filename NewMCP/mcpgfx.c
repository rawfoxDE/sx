void BevBox(char inv, int left, int top, int right, int bot, char * s, int textpen)
{
	WORD l,i;

	if(inv)SetAPen(rp,2); else SetAPen(rp,1);
	Move(rp,left,top);
	Draw(rp,right,top);	/* top line */
	Move(rp,left,top);	/* left line */
	Draw(rp,left,bot);
	if(inv)SetAPen(rp,1); else SetAPen(rp,2);
	Move(rp,left,bot);
	Draw(rp,right,bot);	/* bottom line */
	Move(rp,right,bot);
	Draw(rp,right,top);	/* right line */
	if(s[0]!='\0' && s)
	{
		SetAPen(rp, 4);
		RectFill(rp, left+1, top+1, right-1, bot-1);
		i = strlen(s);
		if(i > 1) l=((right-left)-(i*8))/2; else l=3;

		SetAPen(rp, 1);
		SetBPen(rp, 4);
		SetDrMd(rp, JAM1);
		Move(rp, left+l, top+8);
		Text(rp, s, i);

		SetAPen(rp, textpen);
		Move(rp, left+2+l, top+8);
		Text(rp, s, i);

		SetDrMd(rp, JAM2);
		SetBPen(rp, 0);
		SetAPen(rp, 3);
		Move(rp, left+1, bot-1);
		Draw(rp, right-1, bot-1);
		Move(rp, right-1, bot-1);
		Draw(rp, right-1, top+1);
	}
}

